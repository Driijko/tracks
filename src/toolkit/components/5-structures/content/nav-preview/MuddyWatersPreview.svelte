  <h2>Muddy Waters</h2>
  <p>
    Muddy Waters was a singer, songwriter and guitarist who deeply impacted the development of blues music. Originally hailing from Mississippi, he settled in Chicago in the 1940s and helped define the style known as Chicago Blues. His use of electric guitar would signal a shift away from acoustic to electric blues, and a shift away from rural blues to urban blues.
  </p>
